

public class dowhileloop {
    //program to that uses do while loop to count numbers from 10 up to 1
    public static void main(String[] args) {
                int count = 10;
        
                System.out.println("Counting down from 10 to 1:");
                
                do {
                    System.out.println(count);
                    count--;
                } while (count >= 1);
            }
        }
        
    
    

