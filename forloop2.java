public class forloop2 {
    // program thta uses for loop to enter 10 numbers ang give out there sum
    public static void main(String[] args) {
                 int sum = 0;
                for (int i = 1; i <= 10; i++) {
                    sum += i;
                }
                System.out.println("Sum of numbers from 1 to 10: " + sum);
            }
        }
          
    

