public class nestedforloop2 {
    // a program thta uses  nested for loop that prints a pattern of asterisks in the shape of a right-angled triangle
 public static void main(String[] args) {
            for (int i = 1; i <= 5; i++) {
                for (int j = 1; j <= i; j++) {
                    System.out.print("* ");
                }
                System.out.println();
            }
        }
    }

     
   

